public class Ejercicio5{
	/*Muestra los números del 320 al 160, contando de 20 en 20 utilizando un bucle
  do-while.*/
	public static void main (String[] args) {
    int j =320;
    
    do{        
        System.out.println("Numero: "+j);
        j -= 20;
      }while(j>=160);    
		
	}
}

